//Arithmetic Operations
let a = 43;
let b = 54;
let c = a+b; //add
let d = a-b; //substract
let e = a*b; //multiply
let f = a/b; //divide
console.log("a + b = ",c);
console.log("a - b = ",d);
console.log("a * b = ",e);
console.log("a / b = ", f);

// Modulus
console.log("a % b = ", a % b);

//Exponentiation
console.log("a**b = ",a**b);

//Unary Operators

//Increment
console.log("Increment: ", c++);

//decrement
console.log("decrement: ", c--);

let x = 5
//post increment
console.log("x++ : ",x++); //5
//here value will be changed after the line
console.log("x: ",x); //6

let x2 = 7;
console.log("x2-- = ",x2--); //7
console.log("x2 = ",x2); //6

//pre decrement
let y = 5;
console.log("--y : ",--y); //4
// here value will be changed first
console.log("y: ",y); //4

let y2 = 7;
console.log("++y2 = ",++y2); //8
console.log("y2 = ",y2); //8
