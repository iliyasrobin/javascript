//Arithmetic Operations
let a = 43;
let b = 54;
let c = a+b; //add
let d = a-b; //substract
let e = a*b; //multiply
let f = a/b; //divide
console.log("a + b = ",c);
console.log("a - b = ",d);
console.log("a * b = ",e);
console.log("a / b = ", f);

// Modulus
console.log("a % b = ", a % b);

//Exponentiation
console.log("a**b = ",a**b);

